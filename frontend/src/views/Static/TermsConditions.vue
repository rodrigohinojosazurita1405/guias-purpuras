export default {
  name: 'TermsConditions',
  mounted() {
    // Deshabilitar clic derecho
    document.addEventListener('contextmenu', this.disableRightClick);
    
    // Deshabilitar selección de texto
    document.addEventListener('selectstart', this.disableSelection);
    
    // Deshabilitar copiar (Ctrl+C / Cmd+C)
    document.addEventListener('copy', this.disableCopy);
    
    // Deshabilitar atajos de teclado
    document.addEventListener('keydown', this.disableShortcuts);
  },
  
  beforeUnmount() {
    // Limpiar event listeners al destruir el componente
    document.removeEventListener('contextmenu', this.disableRightClick);
    document.removeEventListener('selectstart', this.disableSelection);
    document.removeEventListener('copy', this.disableCopy);
    document.removeEventListener('keydown', this.disableShortcuts);
  },
  
  methods: {
    disableRightClick(e) {
      e.preventDefault();
      return false;
    },
    
    disableSelection(e) {
      e.preventDefault();
      return false;
    },
    
    disableCopy(e) {
      e.preventDefault();
      return false;
    },
    
    disableShortcuts(e) {
      // Ctrl+C, Ctrl+A, Ctrl+U, F12, Ctrl+Shift+I
      if (
        (e.ctrlKey && (e.key === 'c' || e.key === 'C')) ||
        (e.ctrlKey && (e.key === 'a' || e.key === 'A')) ||
        (e.ctrlKey && (e.key === 'u' || e.key === 'U')) ||
        (e.ctrlKey && e.shiftKey && (e.key === 'i' || e.key === 'I')) ||
        e.key === 'F12'
      ) {
        e.preventDefault();
        return false;
      }
    }
  }
}
